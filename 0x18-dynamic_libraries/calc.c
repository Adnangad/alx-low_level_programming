#include "main.h"

/**
 * add - adds two numbers
 * @a:first no
 * @b:second num
 *
 * Return:sum of the two
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two numbers
 * @a:first no
 * @b:second num
 *
 * Return:diference of the two
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two numbers
 * @a:first no
 * @b:second num
 *
 * Return:result of the two
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * div - divides two numbers
 * @a:first no
 * @b:second num
 *
 * Return:result of the two
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - divides two numbers and gets rem
 * @a:first no
 * @b:second num
 *
 * Return:remainder
 */
int mod(int a, int b)
{
        return (a % b);
}
